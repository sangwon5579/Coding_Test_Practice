SELECT R.FOOD_TYPE, R.REST_ID, R.REST_NAME, R.FAVORITES
FROM  REST_INFO  R 
        JOIN (
            SELECT FOOD_TYPE, MAX(FAVORITES) AS FAVORITES
            FROM REST_INFO
            GROUP BY FOOD_TYPE
        ) RI ON
        R.FOOD_TYPE = RI.FOOD_TYPE
        AND
        R.FAVORITES = RI.FAVORITES
ORDER BY FOOD_TYPE DESC 